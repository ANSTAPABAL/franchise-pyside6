"""настройка логирования для всего приложения.

вызывается один раз при старте — и все модули сразу пишут логи
в одном формате.
"""

import logging


def configure_logging() -> None:
    """включает логирование с единым форматом для всего проекта.

    фишка: basicConfig настраивает корневой логгер, поэтому все остальные
    логгеры в сервисах и страницах наследуют этот формат автоматически —
    не нужно настраивать каждый по отдельности.

    пример строки в логе:
        2026-03-10 12:00:00 | INFO | services.auth_service | сообщение
    """
    logging.basicConfig(
        level=logging.INFO,  # debug-сообщения не выводятся, только info и выше
        format='%(asctime)s | %(levelname)s | %(name)s | %(message)s',
    )
